package aula05.especial;

/**
 * fazer um programa utilizando a estrutura de controle if para verificar se um
 * carro tem combustível. Se sim escrever que o carro vai andar senão escrever
 * que o carro precisa de combustível.
 *
 * @author fabricio
 */
public class Exercicio01 {

    public static void main(String[] args) {

        boolean possuiCombustivel = false;

        if (possuiCombustivel == true) {
            System.out.println("O carro vai andar.");
        } else {
            System.out.println("O carro precisa de combustível.");
        }

    }
}
